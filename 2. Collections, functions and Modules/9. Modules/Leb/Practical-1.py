"""
Write a Python program to import the math module and use functions like sqrt(), ceil(), floor().
"""

import math

num = 7.8

# Get Sqrt() value 
sqrt_value = math.sqrt(num)

# Get ceil() value
ceil_value = math.ceil(num)

# Get floor() value
foolr_value = math.floor(num)

print(f"Square root of {num} is {sqrt_value}")
print(f"Ceiling of {num} is {ceil_value}")
print(f"Floor of {num} is {foolr_value}")