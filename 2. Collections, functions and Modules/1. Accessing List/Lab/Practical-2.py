"""
Write a Python program to access elements at different index positions.
"""

fruits = ['apple', 'banana', 'mango', 'orange', 'grape']

# using index positions
print("Element at index 0:", fruits[0])    
print("Element at index 2:", fruits[2])    
print("Element at index 4:", fruits[4])    

# last element using negative indexing
print("Last element:", fruits[-1])         

# second last element
print("Second last element:", fruits[-2])
