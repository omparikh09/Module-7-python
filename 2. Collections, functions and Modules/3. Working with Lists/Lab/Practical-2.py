"""
Write a Python program to sort a list using both sort() and sorted().
"""

l1 = [1, 4, 5, 2, 3, 6, 8, 0, 9]

# Sort a list using sort()
l1.sort()

print(f"Sorted list using sort() :- {l1}")

l2 = [8, 3, 9, 1, 7, 4]

# Sort a list using sorted()
sort_num = sorted(l2)

print(f"Sorted list using sorted() :- {sort_num}")