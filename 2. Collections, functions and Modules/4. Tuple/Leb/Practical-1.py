"""
Write a Python program to create a tuple with multiple data types.
"""
# tuple with multiple data types
t1 = (9, 78.9, "Python", True, [1, 2, 3], {"Name": "Om", "age": 22}, {1, 2, 3})


print("Tuple with multipule data types :- ")
print(f"Integer Value :- {t1[0]}")              # int
print(f"Float Value :- {t1[1]}")                # folat
print(f"String Value :- {t1[2]}")               # str
print(f"Boolean Value :- {t1[3]}")              # bool
print(f"List inside the tuple :- {t1[4]}")      # list
print(f"Dictionary inside the tuple :- {t1[5]}")# dict
print(f"Set inside the tuple :- {t1[6]}")       # set
